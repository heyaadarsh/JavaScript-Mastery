// Adding two or more strings
let firstName = "Aadarsh";
let lastName = "Giri"

let fullName = firstName + " " + lastName; // " " - Used for giving space
console.log(fullName);

// Playing with types
let val1 = "10";
let val2 = "20";

console.log(val1 + val2); // It will add as a string, so output will be "1020".
console.log(+val1 + +val2); // Using "+" symbol we converted the string into the number, so the output will be 30.